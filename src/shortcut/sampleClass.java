final class ElectricBill {


    final String name;

    final long amount;
    final int id;
    final String adderss;

    public ElectricBill(int id, String name, long amount, String adderss) {
        this.id = id;
        this.name = name;
        this.amount = amount;
        this.adderss = adderss;
    }


    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public long getAmount() {
        return amount;
    }

    public String getAdderss() {
        return adderss;
    }
}

